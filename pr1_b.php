// Aim : PHP program for swapping of two numbers.

<?php
$a = 7;
$b = 11;
echo "Before Swapping : a = ".$a."\n";
echo "Before Swapping : b = ".$b."\n";
$temp = $a;
$a = $b;
$b = $temp;
echo "After Swapping : a = ".$a."\n";
echo "After Swapping : b = ".$b."\n";
?>
